    public boolean UsuarioExistente(String Username){
        int NumeroRepetidos = 0;
        for (int i = 0; i < ListaUsuarios.size(); i++) {
            if (ListaUsuarios.get(i).getUserName().equals(Username)) {
                NumeroRepetidos++;
            }
        }
        
        if (NumeroRepetidos == 0) {
            return false;
        }else{
            return true;
        }
    }
    
